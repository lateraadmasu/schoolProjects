public class Alpha {
         public static void main(String[] args) {

         String one = args[0];

         System.out.println("Enter Greek Letter");

         if (one.equals("alpha")){
           System.out.println("alpha translates to A");
         }

         else if (one.equals("beta")){
           System.out.println(" beta translates to B");
         }

         else if (one.equals("gamma")){
           System.out.println(" gamma translates to C");
         }

         else if (one.equals("delta")){
           System.out.println(" delta translates to D");
         }

         else if (one.equals("epsilon")){
           System.out.println("epsilon translates to E");

         } else {
           System.out.println(" String is not found");
         }

      }  // main method
}  // Hello Alpha
